''' 5)  Conversão de idade Peça a idade do usuário e mostre quantos anos,
    meses e dias ele já viveu. (Considere 1 ano = 365 dias) '''

idade = int(input('Digite sua Idade: '))
print('===================================')

print('Você viveu', idade, 'anos')

print('Voce viveu', idade*12, 'meses')

print('Voce viveu', idade*365, 'dias')